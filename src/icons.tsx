export const CategoryGlyph = ({ cat, size = 16 }: { cat: string; size?: number }) => {
  switch (cat) {
    case "code": return <CodeGlyphIcon size={size} />;
    case "web": return <WebGlyphIcon size={size} />;
    case "data": return <DataGlyphIcon size={size} />;
    case "docs": return <DocsGlyphIcon size={size} />;
    case "image": return <ImageGlyphIcon size={size} />;
    case "media": return <MediaGlyphIcon size={size} />;
    case "archive": return <ArchiveGlyphIcon size={size} />;
    default: return <FileGlyphIcon size={size} />;
  }
};

